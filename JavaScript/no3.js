// Buat variabel dengan nama nilai dan berikan nilai 75
var nilai = 75;

// Gunakan pengkondisian if-else untuk mengecek apakah nilai tersebut lebih besar atau sama dengan 70
if (nilai >= 70) {
    // Jika iya, tampilkan "Lulus" menggunakan console.log()
    console.log("Lulus");
} else {
    // Jika tidak, tampilkan "Tidak Lulus" menggunakan console.log()
    console.log("Tidak Lulus");
}
