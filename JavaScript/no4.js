// Gunakan perulangan for untuk menampilkan angka genap dari 1 hingga 10
for (var i = 1; i <= 10; i++) {
    // Periksa apakah i adalah angka genap dengan memeriksa sisa bagi i dengan 2
    if (i % 2 === 0) {
        // Jika i adalah angka genap, tampilkan menggunakan console.log()
        console.log(i);
    }
}
